#Estructura if compuesta para condiciones falsa y verdadera
#programa para evaluar calificaion capturar si es mayor a 6.0 aprobado
#en caso contrario reprobado

calificacion = float(input("Introduce tu calificacion: "))
if (calificacion>=6.0):
    print("Estas aprobado :D")
    print("Tu calificacion es: ",calificacion)
else:
    print("Estas reprobado D:")
    print("Tu calificacion es: ",calificacion)